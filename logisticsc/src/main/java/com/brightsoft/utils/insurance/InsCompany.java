package com.brightsoft.utils.insurance;

public class InsCompany {

	private String insComTag;
	private String insComName;
	private String insComLogo;
	public String getInsComTag() {
		return insComTag;
	}
	public void setInsComTag(String insComTag) {
		this.insComTag = insComTag;
	}
	public String getInsComName() {
		return insComName;
	}
	public void setInsComName(String insComName) {
		this.insComName = insComName;
	}
	public String getInsComLogo() {
		return insComLogo;
	}
	public void setInsComLogo(String insComLogo) {
		this.insComLogo = insComLogo;
	}
}
